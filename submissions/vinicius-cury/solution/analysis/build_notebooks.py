        "axes[0].set_xlabel('MAE no Teste')\n"
        "axes[0].set_title('MAE por Experimento (menor = melhor)')\n"
        "axes[0].legend()\n"
        "axes[0].invert_yaxis()\n\n"
        "r2s = [r['r2_test'] for r in sorted_res]\n"
