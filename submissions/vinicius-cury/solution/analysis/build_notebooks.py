        "    'R2_Teste': r['r2_test'], 'Accuracy': r['accuracy_test'], 'MAE_CV': r['mae_cv']\n"
        "} for r in results]).sort_values('MAE_Teste')\n\n"
        "print('Ranking por MAE (menor = melhor):')\n"
        "print(results_df.to_string(index=False, float_format='%.4f'))\n\n"
