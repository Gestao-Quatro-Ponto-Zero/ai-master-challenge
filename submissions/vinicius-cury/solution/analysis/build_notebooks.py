        "print(f'Dataset carregado: {len(df_raw)} tickets')\n\n"
        "df = df_raw[df_raw['Ticket Status'] == 'Closed'].copy()\n"
        "print(f'Tickets Closed: {len(df)}')\n\n"
        "df['First Response Time'] = pd.to_datetime(df['First Response Time'], errors='coerce')\n"
