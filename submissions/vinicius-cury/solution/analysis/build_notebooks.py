"""Build notebooks 05, 06, 07 for OptiFlow ML experiments."""
import json
import os

def build_notebook(cells, filename, kernel="optiflow"):
    nb = {
        "nbformat": 4, "nbformat_minor": 5,
        "metadata": {
            "kernelspec": {"display_name": kernel, "language": "python", "name": kernel},
            "language_info": {"name": "python", "version": "3.9.6"}
        },
        "cells": cells
    }
    with open(filename, 'w') as f:
        json.dump(nb, f, indent=1, ensure_ascii=False)
    print(f"Created {filename} with {len(cells)} cells")

def md(text):
    return {'cell_type': 'markdown', 'metadata': {}, 'source': [text]}

def code(text):
    return {'cell_type': 'code', 'metadata': {}, 'source': [text], 'execution_count': None, 'outputs': []}


