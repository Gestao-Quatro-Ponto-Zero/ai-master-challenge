        "    {'name': 'B2: +Duration Int', 'block': 'B', 'features': ['Ticket Channel', 'Ticket Subject', 'Ticket Priority', 'Ticket Type', 'duration_hours', 'age_bucket', 'channel_subject', 'duration_bucket']},\n"
        "    {'name': 'B3: Full Int', 'block': 'B', 'features': ['Ticket Channel', 'Ticket Subject', 'Ticket Priority', 'Ticket Type', 'duration_hours', 'age_bucket', 'channel_subject', 'duration_bucket', 'product_freq']},\n"
        "    {'name': 'C1: +Product Freq', 'block': 'C', 'features': ['Ticket Channel', 'Ticket Subject', 'Ticket Priority', 'Ticket Type', 'product_freq']},\n"
        "    {'name': 'C2: +Product Top10', 'block': 'C', 'features': ['Ticket Channel', 'Ticket Subject', 'Ticket Priority', 'Ticket Type', 'product_top10']},\n"
