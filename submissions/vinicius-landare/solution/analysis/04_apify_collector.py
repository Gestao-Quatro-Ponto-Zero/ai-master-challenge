"""
Fase 4 — Coleta de Dados Reais via Apify (5 plataformas)
==========================================================
Coleta posts de perfis reais via Apify para todas as plataformas do dataset.
Normaliza para o mesmo schema para cruzamento com o dataset.

Plataformas suportadas:
- Instagram: apify/instagram-scraper
- TikTok: clockworks/tiktok-scraper
- YouTube: streamers/youtube-scraper
- RedNote: easyapi/all-in-one-rednote-xiaohongshu-scraper
- Bilibili: kuaima/bilibili-detail

Uso: python 04_apify_collector.py --profile @perfil --platform instagram
"""

import json
import os
import sys
import argparse
from datetime import datetime

BASE_DIR = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
OUTPUT_DIR = os.path.join(BASE_DIR, "analysis", "outputs")
os.makedirs(OUTPUT_DIR, exist_ok=True)

APIFY_TOKEN = os.environ.get("APIFY_TOKEN", "")

ACTORS = {
    "instagram": "apify/instagram-scraper",
    "tiktok": "clockworks/tiktok-scraper",
    "youtube": "streamers/youtube-scraper",
    "rednote": "easyapi/all-in-one-rednote-xiaohongshu-scraper",
    "bilibili": "kuaima/bilibili-detail",
}


def get_apify_client():
    try:
        from apify_client import ApifyClient
    except ImportError:
        print("ERRO: apify-client nao instalado. Rode: pip install apify-client")
        sys.exit(1)

    if not APIFY_TOKEN:
        print("ERRO: APIFY_TOKEN nao configurado. Exporte a variavel ou adicione ao .env")
        sys.exit(1)

    return ApifyClient(APIFY_TOKEN)


