def analyze_collected(posts):
    if not posts:
        return {"error": "Nenhum post coletado"}

    import numpy as np
    engagements = [p["engagement_rate"] for p in posts]
    sorted_posts = sorted(posts, key=lambda x: x["engagement_rate"], reverse=True)

    return {
        "total_posts": len(posts),
        "avg_engagement": round(float(np.mean(engagements)), 2),
        "median_engagement": round(float(np.median(engagements)), 2),
        "std_engagement": round(float(np.std(engagements)), 2),
        "avg_views": round(float(np.mean([p["views"] for p in posts])), 0),
        "avg_likes": round(float(np.mean([p["likes"] for p in posts])), 0),
        "top_3_posts": [
            {"description": p["content_description"][:80], "engagement": p["engagement_rate"], "url": p["content_url"]}
            for p in sorted_posts[:3]
        ],
        "worst_3_posts": [
            {"description": p["content_description"][:80], "engagement": p["engagement_rate"], "url": p["content_url"]}
            for p in sorted_posts[-3:]
        ],
    }


COLLECTORS = {
    "instagram": (collect_instagram, normalize_instagram),
    "tiktok": (collect_tiktok, normalize_tiktok),
    "youtube": (collect_youtube, normalize_youtube),
    "rednote": (collect_rednote, normalize_rednote),
    "bilibili": (collect_bilibili, normalize_bilibili),
}


def main():
    parser = argparse.ArgumentParser(description="Coleta dados de perfil via Apify")
    parser.add_argument("--profile", required=True, help="Username ou URL do perfil")
    parser.add_argument("--platform", required=True, choices=list(ACTORS.keys()))
    parser.add_argument("--max-posts", type=int, default=30)
    args = parser.parse_args()

    collector, normalizer = COLLECTORS[args.platform]

    raw = collector(args.profile, args.max_posts)
    if not raw:
        print("Nenhum post coletado. Verifique o token e o perfil.")
        sys.exit(1)

    posts = normalizer(raw, args.profile)
    analysis = analyze_collected(posts)

    output = {
        "_meta": {
            "generated_at": datetime.now().isoformat(),
            "data_source": "apify_live",
            "profile": args.profile,
            "platform": args.platform,
            "actor": ACTORS[args.platform],
            "posts_collected": len(posts),
        },
        "analysis": analysis,
        "posts": posts,
    }

    filename = f"apify_{args.platform}_{args.profile.lstrip('@').replace('/', '_')}.json"
    path = os.path.join(OUTPUT_DIR, filename)
    with open(path, "w", encoding="utf-8") as f:
        json.dump(output, f, indent=2, ensure_ascii=False, default=str)

    print(f"\nSalvo em: {path}")
    print(f"Posts: {analysis['total_posts']}")
    print(f"Engagement medio: {analysis['avg_engagement']}%")
    if analysis['top_3_posts']:
        print(f"Top post: {analysis['top_3_posts'][0]['engagement']}%")


if __name__ == "__main__":
    main()
