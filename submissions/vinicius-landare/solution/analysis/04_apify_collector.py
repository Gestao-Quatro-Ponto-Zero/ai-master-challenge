def collect_instagram(profile, max_posts=30):
    client = get_apify_client()
    username = profile.lstrip("@")
    print(f"Coletando posts de @{username} no Instagram...")

    run_input = {
        "addParentData": False,
        "directUrls": [f"https://www.instagram.com/{username}/"],
        "resultsLimit": max_posts,
        "resultsType": "posts",
        "searchLimit": 1,
        "searchType": "hashtag",
    }

    run = client.actor(ACTORS["instagram"]).call(run_input=run_input)
    items = list(client.dataset(run["defaultDatasetId"]).iterate_items())
    print(f"Coletados {len(items)} posts")
    return items


