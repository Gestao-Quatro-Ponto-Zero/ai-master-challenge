def normalize_instagram(raw_posts, profile):
    normalized = []
    for i, post in enumerate(raw_posts):
        likes = post.get("likesCount", 0) or 0
        comments = post.get("commentsCount", 0) or 0
        views = post.get("videoViewCount", 0) or post.get("videoPlayCount", 0) or 0
        shares = 0

        if views == 0:
            views = max(likes * 10, 1)

        engagement_rate = ((likes + shares + comments) / views) * 100 if views > 0 else 0

        normalized.append({
            "id": i + 1,
            "platform": "Instagram",
            "content_id": post.get("id", f"ig_{i}"),
            "creator_name": profile.lstrip("@"),
            "content_url": post.get("url", ""),
            "content_type": post.get("type", "image"),
            "post_date": post.get("timestamp", ""),
            "content_description": (post.get("caption", "") or "")[:200],
            "hashtags": ",".join(post.get("hashtags", []) or []),
            "views": views,
            "likes": likes,
            "shares": shares,
            "comments_count": comments,
            "follower_count": post.get("ownerFollowerCount", 0) or 0,
            "is_sponsored": post.get("isPaid", False),
            "engagement_rate": round(engagement_rate, 4),
        })
    return normalized


def normalize_tiktok(raw_posts, profile):
    normalized = []
    for i, post in enumerate(raw_posts):
        likes = post.get("diggCount", 0) or post.get("likes", 0) or 0
        comments = post.get("commentCount", 0) or post.get("comments", 0) or 0
        shares = post.get("shareCount", 0) or post.get("shares", 0) or 0
        views = post.get("playCount", 0) or post.get("plays", 0) or 1

        engagement_rate = ((likes + shares + comments) / views) * 100 if views > 0 else 0

        # Extrair hashtags
        hashtags = []
        for h in (post.get("hashtags", []) or []):
            if isinstance(h, dict):
                hashtags.append(h.get("name", ""))
            elif isinstance(h, str):
                hashtags.append(h)

        normalized.append({
            "id": i + 1,
            "platform": "TikTok",
            "content_id": post.get("id", f"tt_{i}"),
            "creator_name": profile.lstrip("@"),
            "content_url": post.get("webVideoUrl", ""),
            "content_type": "video",
            "post_date": post.get("createTimeISO", ""),
            "content_description": (post.get("text", "") or post.get("desc", "") or "")[:200],
            "hashtags": ",".join(hashtags),
            "views": views,
            "likes": likes,
            "shares": shares,
            "comments_count": comments,
            "follower_count": post.get("authorMeta", {}).get("fans", 0) if isinstance(post.get("authorMeta"), dict) else 0,
            "is_sponsored": False,
            "engagement_rate": round(engagement_rate, 4),
        })
    return normalized


def normalize_youtube(raw_posts, profile):
    normalized = []
    for i, post in enumerate(raw_posts):
        # YouTube scraper retorna formato diferente
        views = post.get("viewCount", 0) or post.get("view_count", 0) or 0
        likes = post.get("likeCount", 0) or post.get("like_count", 0) or 0
        comments = post.get("commentCount", 0) or post.get("reply_count", 0) or 0
        shares = 0

        # Tentar parsear contagens em texto (ex: "76.7万")
        if isinstance(views, str):
            views = _parse_count(views)
        if isinstance(likes, str):
            likes = _parse_count(likes)
        if isinstance(comments, str):
            comments = _parse_count(comments)

        views = max(views, 1)
        engagement_rate = ((likes + shares + comments) / views) * 100 if views > 0 else 0

        normalized.append({
            "id": i + 1,
            "platform": "YouTube",
            "content_id": post.get("id", f"yt_{i}"),
            "creator_name": post.get("author", profile.lstrip("@")),
            "content_url": post.get("url", ""),
            "content_type": "video",
            "post_date": post.get("pubdate", post.get("date", "")),
            "content_description": (post.get("title", "") or post.get("description", "") or "")[:200],
            "hashtags": ",".join(post.get("tags", []) or []),
            "views": views,
            "likes": likes,
            "shares": shares,
            "comments_count": comments,
            "follower_count": 0,
            "is_sponsored": False,
            "engagement_rate": round(engagement_rate, 4),
        })
    return normalized


def normalize_rednote(raw_posts, profile):
    normalized = []
    for i, post in enumerate(raw_posts):
        post_data = post.get("postData", post.get("item", {}))
        note_card = post_data.get("note_card", post_data)

        user = note_card.get("user", {})
        interact = note_card.get("interact_info", note_card.get("interactInfo", {}))

        likes_raw = interact.get("liked_count", interact.get("likedCount", 0))
        likes = _parse_count(likes_raw) if isinstance(likes_raw, str) else (likes_raw or 0)
        views = max(likes * 10, 1)  # RedNote nao mostra views publicamente
        comments = 0
        shares = 0

        engagement_rate = ((likes + shares + comments) / views) * 100 if views > 0 else 0

        title = note_card.get("display_title", post_data.get("displayTitle", ""))
        creator = user.get("nick_name", user.get("nickName", user.get("nickname", profile.lstrip("@"))))

        normalized.append({
            "id": i + 1,
            "platform": "RedNote",
            "content_id": post_data.get("id", note_card.get("noteId", f"rn_{i}")),
            "creator_name": creator,
            "content_url": post.get("link", post_data.get("postUrl", "")),
            "content_type": note_card.get("type", "normal"),
            "post_date": post.get("scrapedAt", ""),
            "content_description": title[:200],
            "hashtags": "",
            "views": views,
            "likes": likes,
            "shares": shares,
            "comments_count": comments,
            "follower_count": 0,
            "is_sponsored": False,
            "engagement_rate": round(engagement_rate, 4),
        })
    return normalized


def normalize_bilibili(raw_posts, profile):
    normalized = []
    for i, post in enumerate(raw_posts):
        views = _parse_count(post.get("view_count", 0))
        likes = _parse_count(post.get("like_count", 0))
        comments = _parse_count(post.get("reply_count", 0))
